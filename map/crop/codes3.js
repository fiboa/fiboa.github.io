// Auto-derive hierarchy-aware, crop-inspired colors
// Rationale:
// - Items that belong to the same hierarchical group (code prefix) should have a coherent base hue.
// - Grasses/forage/pasture/cereals lean green. Arable/fallow/soil lean brown/tan.
// - Use crop-inspired hues for recognizable crops (corn/maize = yellow, carrots/pumpkins/oranges = orange, berries/cherries = red, grapes = purple, etc.).
// - Ensure uniqueness by varying saturation/lightness deterministically from the full code.

// Convert HSL to hex color string
function hslToHex(h, s, l) {
  s /= 100; l /= 100;
  const c = (1 - Math.abs(2 * l - 1)) * s;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = l - c / 2;
  let r = 0, g = 0, b = 0;
  if (0 <= h && h < 60) { r = c; g = x; b = 0; }
  else if (60 <= h && h < 120) { r = x; g = c; b = 0; }
  else if (120 <= h && h < 180) { r = 0; g = c; b = x; }
  else if (180 <= h && h < 240) { r = 0; g = x; b = c; }
  else if (240 <= h && h < 300) { r = x; g = 0; b = c; }
  else { r = c; g = 0; b = x; }
  const to255 = v => Math.round((v + m) * 255);
  const toHex = v => v.toString(16).padStart(2, '0');
  return `#${toHex(to255(r))}${toHex(to255(g))}${toHex(to255(b))}`;
}

// Simple deterministic hash from string -> [0, 1]
function hash01(str) {
  let h = 2166136261;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  // Map to 0..1
  return ((h >>> 0) % 10000) / 10000;
}

// Extract hierarchical parents by trimming trailing non-zero digits to zeros in steps
function computeParents(code) {
  const parents = [];
  const steps = [2, 4, 6, 8, 10];
  for (const l of steps) {
    const parent = code.slice(0, l).padEnd(code.length, '0');
    if (parent !== code) parents.push(parent);
  }
  return parents;
}

// Choose a base hue from crop name keywords
function keywordHue(name) {
  const n = name.toLowerCase();
  const has = kw => n.includes(kw);
  // Specific crops first
  if (has('maize') || has('corn') || has('popcorn')) return 48; // yellow
  if (has('sunflower')) return 50; // sunflower yellow
  if (has('orange') || has('pumpkin') || has('carrot')) return 28; // orange
  if (has('cherry') || has('strawberry') || has('raspberry') || has('tomato') || has('pepper')) return 0; // red
  if (has('grape') || has('vineyard') || has('vine')) return 280; // purple
  if (has('blueberry') || has('bilberry')) return 220; // blue
  if (has('lemon') || has('banana')) return 56; // yellow-ish
  if (has('apple') || has('pear') || has('plum') || has('apricot') || has('peach') || has('nectarine')) return 12; // warm fruit hue
  if (has('beet')) return 350; // beet red
  if (has('rapeseed') || has('canola')) return 55; // bright yellow rapeseed
  if (has('potato')) return 30; // tan
  if (has('soy') || has('soya') || has('pea') || has('bean') || has('lentil')) return 110; // green legumes
  if (has('lettuce') || has('cabbage') || has('spinach') || has('leaf') || has('salad')) return 120; // leafy greens
  if (has('onion') || has('garlic') || has('leek')) return 42; // yellow-brown
  if (has('rice')) return 105; // paddy green
  if (has('wheat') || has('barley') || has('oat') || has('rye') || has('spelt') || has('triticale') || has('millet') || has('sorghum') || has('teff')) return 95; // cereal greenish
  if (has('grass') || has('pasture') || has('meadow') || has('forage') || has('hay')) return 120; // grasses
  if (has('forest') || has('wood') || has('tree') || has('willow') || has('oak') || has('birch')) return 135; // forest green
  if (has('mushroom') || has('truffle')) return 20; // earthy
  if (has('greenhouse')) return 170; // aqua/teal
  if (has('fallow') || has('arable') || has('bare') || has('soil')) return 30; // brown/tan
  if (has('flower') || has('horticulture')) return 330; // magenta/pinkish
  return null;
}

// Fallback hues per broad code family (by the 4-digit prefix)
function familyHue(code) {
  const p4 = code.slice(0, 4);
  switch (p4) {
    case '3301': return 100; // arable crops + cereals → greenish
    case '3302': return 30;  // horticulture/open field → warm brown
    case '3303': return 20;  // permanent crops (orchards/vineyards) → warm
    case '3304': return 25;  // energy/mushrooms/GM → earthy
    case '3305': return 170; // greenhouse/foil → aqua
    case '3306': return 135; // tree/wood/forest → forest green
    case '3307': return 25;  // peat/turf → earthy brown
    case '3308': return 0;   // unmaintained → muted red
    case '3399': return 210; // not known/other → muted blue
    default: return 40;      // general agri → tan
  }
}

// Compute HSL from base hue and code hash; return hex
function deriveColor(baseHue, code) {
  // Vary saturation and lightness within pleasant ranges
  const hJitter = Math.floor(hash01(code) * 16) - 8; // ±8°
  const s = 55 + Math.floor(hash01(code + 's') * 25); // 55–80
  const l = 45 + Math.floor(hash01(code + 'l') * 15); // 45–60
  let h = (baseHue + hJitter) % 360; if (h < 0) h += 360;
  return hslToHex(h, s, l);
}

// Build a quick map from code to item for parent lookup
const itemByCode = Object.fromEntries(hcat.map(i => [i.code, i]));

// Determine color per item
for (const item of hcat) {
  const kHue = keywordHue(item.name);
  let baseHue = kHue;
  if (baseHue == null) {
    // Try to inherit from the closest parent in the hierarchy that got a keyword hue
    const parents = computeParents(item.code);
    for (const p of parents) {
      const parent = itemByCode[p];
      if (parent) {
        const ph = keywordHue(parent.name);
        if (ph != null) { baseHue = ph; break; }
      }
    }
  }
  if (baseHue == null) baseHue = familyHue(item.code);
  item.color = deriveColor(baseHue, item.code);
}